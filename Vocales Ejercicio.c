// Realize un programa que pida el ingreso de un numero del uno al cinco, y que devuelva por pantalla la vocal correspondiente a ese numero
#include <stdio.h>
int main ()
{
printf ("Ingrese un numero del 1 al 5");
int x;
scanf ("%i",&x);
if (x==1) {
printf ("a\n");
}
if (x==2) {
printf ("e\n");
}
if (x==3) {
printf 	("i\n");
}
if (x==4) {
printf ("o\n");
}
if (x==5) {
printf ("u\n");	
}
system ("pause");
return 0;
}

